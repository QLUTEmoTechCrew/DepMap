import numbers

import numpy as np
import torch
from einops import rearrange
from torch import nn
import torch.nn.functional as F

class guide_attention(nn.Module):
    def __init__(self, img_c=64, img_hw=32):
        super().__init__()
        self.img_c = img_c
        self.img_hw = img_hw

        self.gamma = nn.Parameter(torch.zeros(1))  # 初始化为 0，用于 residual 控制

    def forward(self, text_sem, img_sem, image_feat):
        B_T, C, H, W = image_feat.shape

        # 2. Cross-modal attention
        Q = text_sem.view(B_T, self.img_c, -1)  # (B*T, C, N)
        K = img_sem.view(B_T, self.img_c, -1)  # (B*T, C, N)
        V = image_feat.view(B_T, self.img_c, -1)  # (B*T, C, N)

        # attention weights: (B*T, C, C)
        attn = torch.matmul(Q, K.transpose(-1, -2)) / np.sqrt(C)
        attn = F.softmax(attn, dim=-1)  # (B*T, C, C)

        # 3. 融合 + 残差
        fused = torch.matmul(attn, V)  # (B*T, C, N)
        fused = fused.view(B_T, C, H, W)
        out = self.gamma * fused + image_feat  # residual fusion

        return out

def to_3d(x):
    return rearrange(x, 'b c h w -> b (h w) c')

def to_4d(x,h,w):
    return rearrange(x, 'b (h w) c -> b c h w',h=h,w=w)

class BiasFree_LayerNorm(nn.Module):
    def __init__(self, normalized_shape):
        super(BiasFree_LayerNorm, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)

        assert len(normalized_shape) == 1

        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return x / torch.sqrt(sigma+1e-5) * self.weight


class LayerNorm(nn.Module):
    def __init__(self, dim, LayerNorm_type):
        super(LayerNorm, self).__init__()
        if LayerNorm_type == 'BiasFree':
            self.body = BiasFree_LayerNorm(dim)
        else:
            self.body = WithBias_LayerNorm(dim)

    def forward(self, x):
        h, w = x.shape[-2:]
        return to_4d(self.body(to_3d(x)), h, w)

class WithBias_LayerNorm(nn.Module):
    def __init__(self, normalized_shape):
        super(WithBias_LayerNorm, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)

        assert len(normalized_shape) == 1

        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.bias = nn.Parameter(torch.zeros(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        mu = x.mean(-1, keepdim=True)
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return (x - mu) / torch.sqrt(sigma+1e-5) * self.weight + self.bias

